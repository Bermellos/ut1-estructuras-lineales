public class Libro 
{
    public String titulo;
    public String codigo; 
    public double precio; 
    public int stock; 
    
    public Libro(String titulo, String codigo, double precio, int stock)
    {
        this.titulo = titulo; 
        this.codigo = codigo; 
        this.precio = precio;
        this.stock = stock; 
    }
    
}
